void PrintImage(int[,] image) // Метод, выводящий картинку из "+" и " " на экран.
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

void FillImage(int row, int col) // Метод, закрашивающий замкнутую область.
{
    if (pic[row, col] == 0) // Если текущий пиксель не закрашен, т.е. = 0.
    {
        pic[row, col] = 1;
        // Далее FillImage вызывает сам себя:
        FillImage(row - 1, col); // вверх
        FillImage(row, col - 1); // влево
        FillImage(row + 1, col); // вниз
        FillImage(row, col + 1); // вправо
    }
}

